import { getAgentColor } from "../../stores/activity";

interface AvatarProps {
  type: "human" | "agent";
  name: string;
  size?: "sm" | "md" | "lg";
}

const sizeClasses = {
  sm: "w-6 h-6 text-xs",
  md: "w-8 h-8 text-sm",
  lg: "w-10 h-10 text-base",
};

export function Avatar({ type, name, size = "md" }: AvatarProps) {
  const sizeClass = sizeClasses[size];

  if (type === "agent") {
    const bgColor = getAgentColor(name);
    return (
      <div
        className={`${sizeClass} ${bgColor} rounded-full flex items-center justify-center text-white font-medium`}
        title={name}
      >
        <AgentIcon agentType={name} size={size} />
      </div>
    );
  }

  // Human avatar - show initials
  const initials = name
    .split(" ")
    .map((n) => n[0])
    .join("")
    .toUpperCase()
    .slice(0, 2);

  return (
    <div
      className={`${sizeClass} bg-gradient-to-br from-blue-500 to-purple-600 rounded-full flex items-center justify-center text-white font-medium`}
      title={name}
    >
      {initials}
    </div>
  );
}

